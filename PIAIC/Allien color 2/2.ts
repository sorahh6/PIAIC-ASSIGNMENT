// Alien Colors #2: Using an if-else chain to determine points earned

// Version 1: Runs the if block
let alien_color_if = 'green';

if (alien_color_if === 'green') {
    console.log("Congratulations! You just earned 5 points for shooting the green alien.");
} else {
    console.log("Congratulations! You just earned 10 points for shooting an alien of a different color.");
}

// Version 2: Runs the else block
let alien_color_else = 'red';

if (alien_color_else === 'red') {
    console.log("Congratulations! You just earned 10 points for shooting the red alien.");
} else {
    console.log("Congratulations! You just earned 10 points for shooting an alien of a different color.");
}
